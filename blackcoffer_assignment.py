def posnegtivity(string):

   positivity_count = 0
   negativity_count = 0
   complex_word_count = 0
   syllable_count = 0
   number_of_characters = 0
   words = string.split()

   filtered_words =  [word for word in words if word not in stop_words] # removing stopwords from the webscraped data

   filtered_string = ' '.join(filtered_words) #joining them back to a string

   sentences = filtered_string.split('.') #sperating the data into sentences and storing in a list

   for word in filtered_words :

     number_of_characters = number_of_characters + len(word)
     syllable_count = syllable_count + syllables.estimate(word)

     if syllables.estimate(word) > 2 :

       complex_word_count = complex_word_count + 1

     if word in positive_words:

       positivity_count = positivity_count + 1

     elif word in negative_words :

       negativity_count = negativity_count - 1


   # using all the formulas in the TextAnalysis doc to obtain the required values

   negativity_count = (negativity_count * -1)
   polarity = ((positivity_count - negativity_count) / ((positivity_count + negativity_count) + 0.000001))
   subjectivity = ((positivity_count + negativity_count) / (len(filtered_words) + 0.000001))
   avg_sentence_length = (len(filtered_words) / len(sentences))
   avg_words_per_sentence = avg_sentence_length
   percentage_complex_words = ((complex_word_count/ len(filtered_words)) * 100)
   fog_index = (0.4*(avg_sentence_length + percentage_complex_words))
   word_count = len(filtered_words)
   avg_syllable_count = (syllable_count/word_count)
   avg_word_length = (number_of_characters / word_count)

   return  [positivity_count , negativity_count  ,  polarity , subjectivity , avg_sentence_length , percentage_complex_words , fog_index ,  avg_words_per_sentence, complex_word_count , word_count , avg_syllable_count , avg_word_length]


def number_of_personal_pronouns(string):

    # Tokenize the sentence into words
    words = word_tokenize(sentence)
    
    # Use part-of-speech tagging to identify pronouns
    pos_tags = pos_tag(words)
    
    # Define personal pronoun tags
    personal_pronoun_tags = ['PRP', 'PRP$', 'WP', 'WP$']
    
    # Count the number of personal pronouns
    count = sum(1 for word, pos_tag in pos_tags if pos_tag in personal_pronoun_tags)
    
    return count




