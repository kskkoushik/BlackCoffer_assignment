def extract(url):  #extracting content in the website using URL of the website



            html_code = requests.get(url).text # Using requests library to get the html code
            soup = BeautifulSoup(html_code , 'lxml')
            heading1 = soup.find('h1' , class_ = 'tdb-title-text')
            heading2 = soup.find('h1' , class_ = 'entry-title')
            main_heading = '' # the articles heading is stored in this variable
            contents = '' # the articles content is stored in this string

            # I found that there are 2 major types of lovely blackcoffer webpages , So two different
            # methods of scraping techniques are used for scraping either of the webpages

            if heading1 != None :

              main_heading = heading1.text
              content = soup.find_all('div' , class_ = "tdb-block-inner td-fix-index")

              for mc in content :

                if mc.find_all('p') != None or mc.find_all('ul') != None or mc.find_all('ol') != None :


                  paras = mc.find_all('p')

                  for p in paras :
                      contents += '\n' + p.text

                  uls = mc.find_all('ul')

                  for unlists in uls :
                      items = unlists.find_all('li')

                      for echitem in items :
                        contents += '\n' + echitem.text

                  ols = mc.find_all('ol')

                  for orlists in ols :
                    items = orlists.find_all('li')

                    for echitem in items :
                          contents += '\n' + echitem.text

            elif heading2 != None :

              main_heading = heading2.text
              content = soup.find_all('div' , class_ = "td-post-content tagdiv-type")

              for mc in content :

                if mc.find_all('p') != None or mc.find_all('ul') != None or mc.find_all('ol') != None :


                  paras = mc.find_all('p')

